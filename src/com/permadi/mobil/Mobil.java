package com.permadi.mobil;

public class Mobil {
	
	private String id_mobil;
	private String no_plat;
	private String jenis_mobil;
	private String merk;
	private String tahun_buat;
	private String warna;
	private String harga;
	
	public String getId_mobil() {
		return id_mobil;
	}
	public void setId_mobil(String id_mobil) {
		this.id_mobil = id_mobil;
	}
	public String getNo_plat() {
		return no_plat;
	}
	public void setNo_plat(String no_plat) {
		this.no_plat = no_plat;
	}
	public String getJenis_mobil() {
		return jenis_mobil;
	}
	public void setJenis_mobil(String jenis_mobil) {
		this.jenis_mobil = jenis_mobil;
	}
	public String getMerk() {
		return merk;
	}
	public void setMerk(String merk) {
		this.merk = merk;
	}
	public String getTahun_buat() {
		return tahun_buat;
	}
	public void setTahun_buat(String tahun_buat) {
		this.tahun_buat = tahun_buat;
	}
	public String getWarna() {
		return warna;
	}
	public void setWarna(String warna) {
		this.warna = warna;
	}
	public String getHarga() {
		return harga;
	}
	public void setHarga(String harga) {
		this.harga = harga;
	}
		
}
